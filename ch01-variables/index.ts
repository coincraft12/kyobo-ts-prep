// ─── CH01: 변수와 타입 ───────────────────────────────────────────────────────

// ┌─────────────────────────────────────────────────────────────────────────┐
// │ 1. 변수 선언: const / let / var                                         │
// │                                                                         │
// │ [이게 뭔지]                                                             │
// │   변수를 선언하는 세 가지 키워드. TypeScript도 JavaScript와 동일하게   │
// │   const / let / var 중 하나를 사용한다.                                 │
// │                                                                         │
// │ [왜 쓰는지]                                                             │
// │   - const: 한 번 할당한 값을 다시 바꾸지 않을 때. 실수로 값을          │
// │     덮어쓰는 버그를 컴파일 타임에 막을 수 있다.                        │
// │   - let: 반복문 카운터, 누산 값 등 재할당이 꼭 필요한 경우만.         │
// │   - var: 함수 스코프라서 블록 안에서 선언해도 밖에서 보인다.           │
// │     호이스팅 문제 때문에 현대 코드에서는 사용하지 않는다.              │
// │                                                                         │
// │ [주의사항]                                                              │
// │   const로 선언한 객체·배열은 재할당은 불가지만,                        │
// │   내부 값(프로퍼티, 요소) 변경은 가능하다.                             │
// │   완전한 불변을 원하면 Object.freeze() 또는 as const를 쓴다.           │
// └─────────────────────────────────────────────────────────────────────────┘

const name = 'Sharon';   // 재할당 불가 → 대부분 이거
let count = 0;           // 재할당 필요할 때만
// var x = 1;            // 절대 안 씀

// ┌─────────────────────────────────────────────────────────────────────────┐
// │ 2. 타입 어노테이션 (Type Annotation)                                    │
// │                                                                         │
// │ [이게 뭔지]                                                             │
// │   변수 이름 뒤에 `: 타입` 을 붙여서 이 변수가 어떤 타입의 값을 담는지 │
// │   명시하는 문법. TypeScript에서만 존재하고 JavaScript에는 없다.        │
// │                                                                         │
// │ [왜 쓰는지]                                                             │
// │   - 잘못된 타입의 값이 들어오면 실행 전에 에러를 잡아준다.            │
// │   - IDE에서 자동완성과 타입 힌트를 정확하게 받을 수 있다.             │
// │   - 코드를 읽는 사람이 이 변수에 무엇이 들어오는지 즉시 이해한다.     │
// │                                                                         │
// │ [문법 해부]                                                             │
// │   const 변수명: 타입 = 값;                                              │
// │   ①         ②  ③   ④                                                  │
// │   ① const: 재할당 불가 선언                                            │
// │   ② 변수명: 사용할 이름                                                │
// │   ③ :타입: 타입 어노테이션. 콜론(:) 뒤에 타입을 쓴다                  │
// │   ④ = 값: 초기값 할당                                                  │
// │                                                                         │
// │ [주의사항]                                                              │
// │   아래 "추론 활용" 예제처럼 초기값이 있으면 타입을 생략해도 된다.     │
// │   TS가 값을 보고 타입을 자동으로 파악한다(타입 추론). 단, 함수 매개변 │
// │   수나 외부에서 들어오는 값은 명시하는 것이 안전하다.                  │
// └─────────────────────────────────────────────────────────────────────────┘

const userId: string = 'user-001';   // string 타입 명시: 문자열만 허용
const age: number = 40;              // number 타입 명시: 정수/소수 모두 포함
const isActive: boolean = true;      // boolean 타입 명시: true 또는 false만 허용

// 추론 활용 (어노테이션 생략해도 TS가 알아서 파악)
// 초기값이 'coincraft'(문자열)이므로 TS가 string으로 자동 추론한다.
// 어노테이션이 없어도 나중에 숫자를 넣으려 하면 에러가 난다.
const platform = 'coincraft';  // 타입: string (추론됨)
const tokenCount = 5;          // 타입: number (추론됨)

// ┌─────────────────────────────────────────────────────────────────────────┐
// │ 3. 배열 타입                                                            │
// │                                                                         │
// │ [이게 뭔지]                                                             │
// │   배열의 원소 타입을 지정하는 문법. 타입[] 또는 Array<타입> 두 가지로  │
// │   쓸 수 있으며, 실무에서는 타입[] 형태를 더 많이 쓴다.                 │
// │                                                                         │
// │ [왜 쓰는지]                                                             │
// │   - 배열에 의도와 다른 타입이 섞이는 것을 막는다.                     │
// │   - .map(), .filter() 등을 호출할 때 원소 타입이 자동으로 전파된다.   │
// │                                                                         │
// │ [문법 해부]                                                             │
// │   string[] → "string의 배열"                                           │
// │   number[] → "number의 배열"                                           │
// │   Array<string> 과 string[] 은 완전히 동일한 의미다.                   │
// └─────────────────────────────────────────────────────────────────────────┘

const walletIds: string[] = ['0xAAA', '0xBBB', '0xCCC'];  // string만 담을 수 있는 배열
const balances: number[] = [100, 200, 300];                // number만 담을 수 있는 배열

// ┌─────────────────────────────────────────────────────────────────────────┐
// │ 4. 객체 타입 (인라인 타입)                                              │
// │                                                                         │
// │ [이게 뭔지]                                                             │
// │   객체 변수에 어떤 키와 값 타입이 들어와야 하는지 인라인으로 명시하는  │
// │   방법. 별도로 interface나 type을 만들지 않고 바로 쓸 때 사용한다.     │
// │                                                                         │
// │ [문법 해부]                                                             │
// │   { name: string; age: number; email?: string }                         │
// │          ①           ②              ③                                  │
// │   ① name: string → name 키는 string 타입                               │
// │   ② age: number → age 키는 number 타입                                 │
// │   ③ email?: string → ?는 "선택적"을 의미. 있어도 되고 없어도 된다.    │
// │     값이 있으면 string, 없으면 undefined.                               │
// │                                                                         │
// │ [주의사항]                                                              │
// │   인라인 타입은 간단한 경우에만 쓰는 것이 좋다. 같은 구조를 여러 곳에 │
// │   서 쓴다면 interface나 type으로 따로 정의해서 재사용하는 것이 낫다.   │
// │   (ch03에서 다룬다)                                                     │
// └─────────────────────────────────────────────────────────────────────────┘

const user: { name: string; age: number; email?: string } = {
  name: 'Sharon',
  age: 40,
  // email 없어도 OK (?:는 선택적)
};

// ┌─────────────────────────────────────────────────────────────────────────┐
// │ 5. 특수 타입: unknown / any / never / void                              │
// │                                                                         │
// │ [unknown]                                                               │
// │   이게 뭔지: "뭔지 모르는 타입". 외부에서 들어오는 데이터,             │
// │     JSON 파싱 결과, API 응답 등에 쓴다.                                 │
// │   JS와의 차이: JS에는 없는 개념. any와 달리 unknown은 타입 검사 없이   │
// │     함부로 사용하면 컴파일 에러가 난다.                                 │
// │   주의: unknown 타입 변수를 쓰려면 반드시 typeof, instanceof 등으로    │
// │     타입을 먼저 확인해야 한다 (아래 실행 확인 부분 참고).              │
// │                                                                         │
// │ [any]                                                                   │
// │   이게 뭔지: 타입 검사를 완전히 끈다. TS가 무슨 값이든 허용한다.      │
// │   왜 쓰지 말아야 하나: any를 쓰는 순간 TypeScript의 안전망이 사라진다. │
// │     "TS를 쓰면서 any만 쓴다"는 JS로 돌아간 것과 같다.                 │
// │                                                                         │
// │ [never]                                                                 │
// │   이게 뭔지: 절대 정상적으로 값을 반환하지 않는 함수의 리턴 타입.     │
// │     항상 예외를 던지거나, 무한루프를 도는 함수에 붙인다.               │
// │   실용: 강의 코드에서 에러를 throw하는 헬퍼 함수에 등장한다.          │
// │                                                                         │
// │ [void]                                                                  │
// │   이게 뭔지: 리턴값이 없는 함수의 리턴 타입. console.log처럼           │
// │     뭔가를 "하기만 하고" 값을 돌려주지 않는 함수에 쓴다.              │
// │   never와의 차이: void는 함수가 정상적으로 끝나지만 반환값이 없는 것. │
// │     never는 함수가 정상적으로 끝나지 않는다는 것 (예외, 무한루프).    │
// └─────────────────────────────────────────────────────────────────────────┘

let payload: unknown = '{"tokenId": "1"}';   // 쓰기 전에 타입 검사 강제
// let bad: any = 1;                          // any = TS 포기 선언, 쓰지 마라

function throwAlways(): never {
  throw new Error('항상 에러');               // never: 절대 정상 리턴 안 함
}

function doNothing(): void {
  console.log('리턴값 없음');                 // void: 리턴 없는 함수
}

// ┌─────────────────────────────────────────────────────────────────────────┐
// │ 6. bigint — 블록체인 필수 타입                                          │
// │                                                                         │
// │ [이게 뭔지]                                                             │
// │   JavaScript의 number는 64비트 부동소수점이라 2^53(약 9천조) 이상의    │
// │   정수를 정확히 표현하지 못한다. bigint는 이 한계 없이 임의 크기의     │
// │   정수를 정확하게 다루는 타입이다.                                      │
// │                                                                         │
// │ [왜 쓰는지]                                                             │
// │   블록체인에서 토큰 ID, 트랜잭션 금액(wei 단위), 타임스탬프 등은       │
// │   number의 안전 정수 범위를 쉽게 초과한다.                              │
// │   이더리움 1 ETH = 10^18 wei → number로 표현하면 정밀도 손실 발생.    │
// │   bigint를 써야 정확한 값을 보장할 수 있다.                            │
// │                                                                         │
// │ [문법 해부]                                                             │
// │   - 리터럴: 숫자 뒤에 n을 붙인다 (예: 1000n, 50_000_000n)             │
// │   - 변환: BigInt(숫자) 또는 BigInt('문자열') 로 만들 수 있다.         │
// │   - 가독성용 _: 1_000_000n 처럼 _ 를 구분자로 쓸 수 있다. 값에 영향  │
// │     없이 큰 숫자를 읽기 쉽게 만든다.                                   │
// │                                                                         │
// │ [주의사항]                                                              │
// │   - bigint와 number는 혼용 불가. `100n + 1`은 컴파일 에러.            │
// │     나누는 값도 반드시 bigint여야 한다 (`amount / 2n`).                │
// │   - DB 저장, API 응답, 로그 출력 시 toString() 또는 String()으로      │
// │     문자열로 변환해서 사용한다.                                         │
// │   - JSON.stringify는 bigint를 직렬화하지 못한다 → 먼저 toString().    │
// └─────────────────────────────────────────────────────────────────────────┘

const tokenId: bigint = 1234567890123456789n;   // 숫자 뒤에 n 붙이면 bigint 리터럴
const amount = 50_000_000n;                       // _는 가독성용 구분자 (값에 영향 없음)
const fee = BigInt('1000000000000000');            // 문자열에서 변환

// bigint 연산
// + - * / % ** 연산자를 모두 지원한다. 단, 피연산자 모두 bigint여야 한다.
const total = amount + fee;                       // + - * / % ** 가능
const half  = amount / 2n;                        // 나누는 쪽도 반드시 bigint (2n, 숫자 아님)
// const wrong = amount + 1;                      // 에러: bigint와 number 혼용 불가

// number → bigint 변환
// 이미 number 타입인 값을 bigint로 바꿀 때는 BigInt()를 쓴다.
// 단, 소수점 있는 number는 변환 불가 (BigInt(1.5) → 에러)
const price = 100;
const priceBig = BigInt(price);                   // BigInt()로 변환

// bigint → string (로그, DB 저장 시)
// bigint는 직접 문자열 연결(+)이나 console.log 포매팅에 주의 필요
const stored = tokenId.toString();   // '1234567890123456789'
const display = String(amount);      // '50000000'

